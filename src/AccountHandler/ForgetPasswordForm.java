     private int generateRandomCode()
    {
        Random rand=new Random();
        
        return rand.nextInt(90000)+10000;
    }
     
     private boolean checkUserExists() throws Exception
     {
         String Username=UsernameTextField.getText();
         String filepath="signup_info" + "_" + Username + ".txt";
         File file=new File(filepath);
         if(file.exists())
         {
             return true;
         }
         else
         {
             throw new Exception("User Not Found");
         }
     }
     
    
    
     private boolean VerifyingCode()
     {
         int Code=AuthCode;
         String CodeString=String.valueOf(Code).trim();
         String EnteredVerificationCode=VerificationCodeTextField.getText();
         
         if(EnteredVerificationCode.equals(CodeString))
         {
             return true;
         }
         return false;
     }
    
