    private void SetNewPasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SetNewPasswordButtonActionPerformed
        String NewPassword=NewPasswordField.getText();
        String Username=UsernameTextField.getText();
        String filepath="signup_info" + "_" + Username + ".txt";
        String line;
        boolean passwordChanged=false;
        try{
            File file=new File(filepath);
            File fileTemp=new File("temp.txt");
            BufferedReader reader=new BufferedReader(new FileReader(file));
            BufferedWriter writer=new BufferedWriter(new FileWriter(fileTemp));
            while((line=reader.readLine())!=null)
            {
                if(line.startsWith("Password: "))
                {
                    writer.write("Password: "+NewPassword);
                    writer.newLine();
                    passwordChanged=true;
                }
                else
                {
                    writer.write(line);
                    writer.newLine();
                    
                }
                    
            }
            writer.close();
            reader.close();
            
            if(passwordChanged)
            {
                file.delete();
                fileTemp.renameTo(file);
                JOptionPane.showMessageDialog(null, "Password Successfully Changed!", "Forget Password", JOptionPane.INFORMATION_MESSAGE);
                Login login=new Login();
                this.setVisible(false);
                login.setVisible(true);
            }
            else
            {
                fileTemp.delete();
                JOptionPane.showMessageDialog(null, "An Error Occurred", "Forget Password", JOptionPane.ERROR_MESSAGE);
            }
            
        }
        catch(IOException IOE)
        {
            JOptionPane.showMessageDialog(null, IOE.getMessage(), "Forget Password", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_SetNewPasswordButtonActionPerformed

